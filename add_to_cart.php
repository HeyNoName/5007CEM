<?php
session_start();

if(isset($_GET['seasonal_id'])) {
    $seasonalId = $_GET['seasonal_id'];

    // You might want to validate the seasonalId to prevent SQL injection

    // Add the product to the cart
    if(!isset($_SESSION['cart'])) {
        $_SESSION['cart'] = array();
    }

    // Assuming you want to store the quantity as well
    if(isset($_SESSION['cart'][$seasonalId])) {
        $_SESSION['cart'][$seasonalId]++;
    } else {
        $_SESSION['cart'][$seasonalId] = 1;
    }

    echo "Product added to the cart successfully!";
} else {
    echo "Invalid request.";
}
?>
